package com.pactera.weather.model;

/**
 * @author Anand
 *
 */
public class Wind {
	private float speed;
	private float deg;

	/**
	 * @return the speed
	 */
	public float getSpeed() {
		return speed;
	}

	/**
	 * @param speed
	 *            the speed to set
	 */
	public void setSpeed(float speed) {
		this.speed = speed;
	}

	/**
	 * @return the deg
	 */
	public float getDeg() {
		return deg;
	}

	/**
	 * @param deg
	 *            the deg to set
	 */
	public void setDeg(float deg) {
		this.deg = deg;
	}

}
